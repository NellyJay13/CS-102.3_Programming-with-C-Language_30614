//Using While Loop

#include <stdio.h>
#include
<stdlib.h>
int main()
{
int x=1;
while(x<=100)
{
printf("%d", x);
x++;
}
return 0;
}
//Using Do While Loop

#include <stdio.h>
#include
<stdlib.h>
int main()
{
int
x=1;
do
{
printf("%d", x);
x++;
}while(x<=100);
return 0;
}
//Using For Loop

#include <stdio.h>
#include
<stdlib.h>
int main()
{
int x;
for(x=1;x<=100;x++)
{
printf("%d", x);
}
return 0;
}
